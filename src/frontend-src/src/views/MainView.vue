<script setup>
import CalendarComponent from "@/components/CalendarComponent.vue";
import CounterComponent from "@/components/CounterComponent.vue";
import TestComponent from "@/components/TestComponent.vue";
//import EventoService from '../service/EventoService.js';
import { ref } from "vue";
import { shallowRef } from "vue";
import { onMounted } from "vue";
import { runModel } from "@/LLM/index.js";


const ShowLeftBar = shallowRef(true);
const contentBlurred = shallowRef(false);
const activeComponent = shallowRef(CalendarComponent);

const toggleLeftBar = () => {
  ShowLeftBar.value = !ShowLeftBar.value;
  contentBlurred.value = !ShowLeftBar.value;
};

const setActiveComponent = (component) => {
  activeComponent.value = component;
};
const tarefas = ref([]);

onMounted(async () => {
  try {
    const response = await EventoService.getTarefas();
    tarefas.value = response.data;
  } catch(error) {
    console.log("Erro getTarefas: ", error);
  }
});

let LLMResponses = ref(null);

const callRunModel = async () => {
  // Ensure tarefas.value is loaded before proceeding
  if (tarefas.value.length === 0) {
    console.error('No tasks available');
    return;
  }
  // Find the first incomplete task
  const taskSelec = findIncompleteTask(tarefas.value);

  // Find all incomplete tasks
  const incompleteTasks = findAllIncompleteTasks(tarefas.value);

  // Check if there are any incomplete tasks
  if (incompleteTasks.length === 0) {
    console.error('No incomplete tasks found');
    return;
  }

  for (const taskSelec of incompleteTasks) {
    const todasTask = tarefas.value.map(task => ({
      horarioInicio: task.horarioInicio,
      horarioFim: task.horarioFim 
    }));

    const response = await runModel(taskSelec, todasTask);
    LLMResponses.value.push(response);
    //console.log(LLMResponses.value)
  }
  
};

onMounted(callRunModel);
  
</script>

<template>
  <div class="main-page">
    <div class="left-bar" :class="{ show: !ShowLeftBar }">
      <input
        class="checkbox"
        @click="toggleLeftBar"
        type="checkbox"
        name=""
        id=""
      />
      <div class="hamburger-lines">
        <span class="line line1"></span>
        <span class="line line2"></span>
        <span class="line line3"></span>
      </div>
      <nav v-if="!ShowLeftBar">
        <a
          @click="setActiveComponent(CalendarComponent)"
          class="nav-link"
          :class="{ activeLink: activeComponent === CalendarComponent }"
          >Calendar
        </a>
        <a
          @click="setActiveComponent(TestComponent)"
          class="nav-link"
          :class="{ activeLink: activeComponent === TestComponent }"
          >Test
        </a>
        <a
          @click="setActiveComponent(CounterComponent)"
          class="nav-link"
          :class="{ activeLink: activeComponent === CounterComponent }"
